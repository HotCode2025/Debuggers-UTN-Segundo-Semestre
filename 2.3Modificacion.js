//Modificacion de elementos en una lista

const autos =["Ferrari", "Renault","Bmw"];
console.log(autos);


//Modificamos un elemento del arreglo
autos[1]="Volvo"
console.log(autos);

//Agregamos elementos al arreglo
autos.push("Audi");
console.log(autos);

//Segunda forma
autos[autos.length]="Porche";
console.log(autos);

//Tercera forma
autos[7]="Renault";
console.log(autos);
