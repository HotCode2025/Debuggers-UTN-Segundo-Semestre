# La palabra return en funciones
# Creamos una funcion para sumar
def sumar(a, b):
    return a + b
resultado = sumar(78, 22) # Guardamos el resultado de la funcion en una variable
# print(f'El resultado de la suma es: {resultado}')
# La palabra return nos permite devolver un valor desde una funcion
print(f'El resultado de la suma es: {sumar(55, 45)}') # Podemos llamar a la funcion dentro de un print
