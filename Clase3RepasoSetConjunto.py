#Repaso de set o Conjunto
# para definir un conjunto
conjunto1 = set()
conjunto2 = {'bye', }
conjunto1.add(7)
conjunto1.add('Hola')
print(conjunto1)
conjunto2.add('adiós')
print(conjunto2)
print(3 not in conjunto2) # Preguntamos si el número 3 NO está en el conjunto1

# Como hacer la igualdad de dos conjuntos
print(conjunto2 == conjunto1) #Nos devuelve como respuesta un booleano