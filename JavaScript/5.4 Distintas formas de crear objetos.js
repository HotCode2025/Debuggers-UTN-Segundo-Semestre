// Diferentes formas de crear objetos
//caso numero 1
let miObjeto = new Object(); //Esta es una opcion formal
//caso numero 2
let miObjeto2 = {}; //Esta es una opcion mas moderna y recomendada

//caso string 1
let miCadena1 = new String('Hola'); //Esta es una opcion formal
//caso string 2
let miCadena2 = 'Hola'; //Esta es una opcion mas moderna y recomendada

//caso numero 1
let miNumero1 = new Number(1); //Esta es una opcion formal
//caso numero 2
let miNumero2 = 1; //Esta es una opcion mas moderna y recomendada

//caso boolean 1
let miBoolean1 = new Boolean(false); //Esta es una opcion formal
//caso boolean 2
let miBoolean2 = false; //Esta es una opcion mas moderna y recomendada

//caso arreglo 1
let miArreglo1 = new Array(); //Esta es una opcion formal
//caso arreglo 2
let miArreglo2 = []; //Esta es una opcion mas moderna y recomendada

//caso funcion 1
let miFuncion1 = new Function(); //Esta es una opcion formal, todo despuies de new es considerado objeto
//caso funcion 2
let miFuncion2 = function(){}; //Esta es una opcion simplificada, mas moderna y recomendada
